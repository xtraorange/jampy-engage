DROP TABLE IF EXISTS employee_mv;

CREATE TABLE employee_mv (
    EMPLOYEE_ID TEXT PRIMARY KEY,
    EFF_DT TEXT,
    EFF_SEQ INTEGER,
    LOCATION TEXT,
    DEPARTMENT_ID TEXT,
    JOB_CODE TEXT,
    JOB_TITLE TEXT,
    NAME TEXT,
    LAST_NAME TEXT,
    FIRST_NAME TEXT,
    MIDDLE_NAME TEXT,
    NAME_PSFORMAT TEXT,
    SUPERVISOR_ID TEXT,
    SUPERVISOR_NAME TEXT,
    SUPERVISOR_JOB_CODE TEXT,
    SUPERVISOR_JOB_TITLE TEXT,
    BU_CODE TEXT,
    COMPANY TEXT,
    STATUS_CODE TEXT,
    ACTION_DT TEXT,
    MIDDLE_INIT TEXT,
    TERMINATION_DT TEXT,
    IS_KEY INTEGER,
    DEPT_LOC_DESCSHRT TEXT,
    HIRE_DT TEXT,
    LAST_HIRE_DT TEXT,
    FULL_PART_TIME TEXT,
    GL_EXPENSE TEXT,
    USERNAME TEXT,
    PREFERRED_NAME TEXT,
    PHONE TEXT,
    NAME_FORMAL_TXT TEXT,
    MANAGER_ID TEXT,
    MOBILE_PHONE TEXT,
    FAX TEXT,
    FAS_RIGHTFAX TEXT,
    EMAIL TEXT,
    EMAIL2 TEXT,
    APPROVER_ID TEXT,
    TREE_BRANCH TEXT
);

CREATE INDEX IF NOT EXISTS idx_employee_mv_username ON employee_mv (USERNAME);
CREATE INDEX IF NOT EXISTS idx_employee_mv_last_first ON employee_mv (LAST_NAME, FIRST_NAME);
CREATE INDEX IF NOT EXISTS idx_employee_mv_supervisor_id ON employee_mv (SUPERVISOR_ID);
CREATE INDEX IF NOT EXISTS idx_employee_mv_status ON employee_mv (STATUS_CODE);
CREATE INDEX IF NOT EXISTS idx_employee_mv_job_code ON employee_mv (JOB_CODE);
